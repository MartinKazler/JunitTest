
public class Calculator {

	public static int plus(int a, int b) {
		return a + b;
	}

	public static int subtrakt(int a, int b) {
		return a - b;
	}

	public static int multiply(int a, int b) {
		return a * b ;
	}

	public static double divide(double a, double b) {
		return a/b;
	}

}
